    func emojiForKeyword(_ keyword: String) -> String {
        // Direct match
        if let emoji = emojiMap[keyword] {
            return emoji
        }
        
        // Check if keyword contains any mapped word
        for (key, emoji) in emojiMap {
            if keyword.contains(key) {
                return emoji
            }
        }
        
        // Return default
        return emojiMap["default"] ?? "✨"
    }
    
    func randomEmoji(for keyword: String) -> String {
        // Generate consistent emoji based on keyword hash
        let emojis = ["✨", "🌟", "💫", "⭐", "🎯", "🎨", "🌈", "🔥", "💎", "🌊"]
        var hash = 0
        for char in keyword.unicodeScalars {
            hash = Int(char.value) + (hash << 6) + (hash << 16) - hash
        }
        return emojis[abs(hash) % emojis.count]
    }
